<b style="color: #e38e0f; font-size: 16px">Q. How can I join north south university photography club?</b>
A. Atfirst, you need to fillup the signup form. Then we will call you for an interview and we will let you know if you are selected.

<b style="color: #e38e0f; font-size: 16px">Q. I am from another university, can I still join NSUPC?</b>
A. Sorry, You have to be enrolled in any programs of NSU. But you can still participate in many of ours Inter-University Photography Contest.

<b style="color: #e38e0f; font-size: 16px">Q. Who selects the winners of the Photography Contests?</b>
A. Our esteemed judges will judge all your photos and select the winners. Our judging Panel consists of award-winning personels.

<b style="color: #e38e0f; font-size: 16px">Q. How many photos can I send into the contest?</b>
A. This varies from different contests to contests but usually we allow 15 photos.

<b style="color: #e38e0f; font-size: 16px">Q. What category should I select for my photograph?</b>
A. There are some predefined categories. You need select from those. They are:
   
   *Natures and Travels
   *People and Lifestyle
   *Fashion and Portraits
   *Product Photography 
   *Mobile Photography
   
<b style="color: #e38e0f; font-size: 16px">Q. Can I enter in the contest if I am a student of other universities?</b>
A. You are most welcome to enter in any of our Inter-University Contests.

<b style="color: #e38e0f; font-size: 16px">Q. What size should my digital image be for uploading with the online entry form?</b>
A. Files submitted may not be larger than 10MB. Photo contest images are judged on computer screens, which have a maximum resolution of 72dpi. 
   When uploading an image, we require a minimum width of 2,000 pixels. Please retain a high-resolution version of your photograph(s) in your 
   personal files. You will need to have a higher-resolution image on hand, a minimum of 300 dpi and 3,000 pixels on the longest side, to be
   considered in the final rounds of the contest.


